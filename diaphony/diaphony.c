/******************************************************************************/

int main ( int argc, char *argv[] )

/******************************************************************************/
/*
  Purpose:

    MAIN is the main program for DIAPHONY.

  Discussion:

    DIAPHONY reads a table dataset and applies the diaphony test.

  Usage:

    diaphony table_file

  Licensing:

    This code is distributed under the GNU LGPL license.

  Modified:

    25 January 2012

  Author:

    John Burkardt
*/
{
  double d;
  double de;
  int dim_num;
  double e;
  char input_filename[255];
  int point_num;
  double *points;
  int verbose = 0;

  if ( verbose )
  {
    timestamp ( );
    printf ( "\n" );
    printf ( "DIAPHONY:\n" );
    printf ( "  C version\n" );
    printf ( "  Compute the diaphony of a point set.\n" );
  }
/*
  Get the input filename
*/
  if ( 1 < argc )
  {
    strcpy ( input_filename, argv[1] );
  }
  else
  {
    printf ( "\n" );
    printf ( "  Enter the input filename:\n" );
    scanf ( "%s", input_filename );
  }
/*
  Get the data size.
*/
  r8mat_header_read ( input_filename, &dim_num, &point_num );

  if ( verbose )
  {
    printf ( "\n" );
    printf ( "  The spatial dimension is %d\n", dim_num );
    printf ( "  The number of points is %d\n", point_num );
  }
/*
  Read the data.
*/
  points = r8mat_data_read ( input_filename, dim_num, point_num );

  if ( r8mat_min ( dim_num, point_num, points ) < 0.0 )
  {
    printf ( "\n" );
    printf ( "DIAPHONY - Fatal error!\n" );
    printf ( "  At least one coordinate of a point is less than 0.\n" );
    exit ( 1 );
  }
  else if ( 1.0 < r8mat_max ( dim_num, point_num, points ) )
  {
    printf ( "\n" );
    printf ( "DIAPHONY - Fatal error!\n" );
    printf ( "  At least one coordinate of a point is greater than 1.\n" );
    exit ( 1 );
  }
/*
  Analyze the data.
*/
  d = diaphony_compute ( dim_num, point_num, points );

  e = 1.0 / sqrt ( ( double ) point_num );
  de = d / e;

  printf ( "\n" );
  printf ( "  File  M  N  Diaphony  1/sqrt(N)  D/sqrt(N)\n" );
  printf ( "  %s  %2d  %5d  %14.6g  %14.6g  %14.6g\n",
    input_filename, dim_num, point_num, d, e, de );

  free ( points );
/*
  Terminate.
*/
  if ( verbose )
  {
    printf ( "\n" );
    printf ( "DIAPHONY:\n" );
    printf ( "  Normal end of execution.\n" );
    printf ( "\n" );
    timestamp ( );
  }

  return 0;
}
